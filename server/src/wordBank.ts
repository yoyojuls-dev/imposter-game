// 1. Pre-combine all categories into one massive array for the Random option.
// Using .flat() merges every category's words together instantly!
const allWords = Object.values(wordBank).flat();

export function getRandomWord(topic: string, difficulty: 'Easy' | 'Medium' | 'Hard', usedWords: string[] = []): { word: string; hint: string } {
  
  // 2. If they pick "Random 🎲", use the massive combined list. 
  // Otherwise, use their specific category (fallback to allWords if topic is missing).
  const entries = topic === 'Random 🎲' ? allWords : (wordBank[topic] || allWords);
  
  // 3. Filter out already used words so we don't get repeats
  const available = entries.filter(e => !usedWords.includes(e.word));
  
  // 4. If all words in the category have been used, reset and use the full list
  const pool = available.length > 0 ? available : entries;
  
  // 5. Pick a random word from the pool
  const entry = pool[Math.floor(Math.random() * pool.length)];
  
  return { word: entry.word, hint: entry.hints[difficulty] };
}